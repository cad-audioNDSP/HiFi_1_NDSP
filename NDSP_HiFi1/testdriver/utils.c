/* 32-bit CRC update for a data block. Number of octets must be even! */
uint32_t crc32( uint32_t crc, const uint8_t * buf, int len )
{
  int k;

  ASSERT( buf && ( len >= 0 ) && !( len & 1 ) );

  for ( k=0; k<len; k+=2 )
  {
    crc = crc32_tab[buf[k+1] ^ (crc>>24)] ^ (crc<<8);
    crc = crc32_tab[buf[k+0] ^ (crc>>24)] ^ (crc<<8);
  }

  return (crc);

} /* crc32() */
