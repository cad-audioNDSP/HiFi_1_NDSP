typedef struct TRand {
    long iPrior;         /* prior value   */
    long uiRand;         /* current value */
} TRand;

static TRand gRand = {0, 0};

void Rand_reset(int a, int b)
{
	gRand.iPrior = a;
	gRand.uiRand = b;
}

int Rand(void)
{
    const unsigned long uiA = 1664525L;
    const unsigned long uiC = 1013904223L;
    long iTemp, iTemp1;

    gRand.uiRand =  uiA * gRand.uiRand + uiC;

    iTemp = ((long)gRand.uiRand) >> 2; 
    iTemp += (iTemp>>2);

    iTemp1 = iTemp - gRand.iPrior;

    gRand.iPrior = iTemp;

    return iTemp1;
}

void Rand_i32(void *x, int n)
{
    int *px = (int *)x;
    int i;
    for(i = 0; i < n; i++) {
        px[i] = Rand();
    }
}

void Rand_i16(void *x, int n)
{
    int16_t *px = (int16_t *)x;
    int i;
    for(i = 0; i < n; i++) {
        px[i] = (int16_t)Rand();
    }
}

int writepcm24(const int32_t *x, int n, FILE *fp)
{
    static char buf[3*256];
    int N = n;
    while(n) {
        int m = sizeof(buf);
        char *pbuf = buf;
        do {
            int val = *x++;
            *pbuf++ = (char)(val>> 8);
            *pbuf++ = (char)(val>>16);
            *pbuf++ = (char)(val>>24);
            m -= 3;
            n -= 1;
        } while(n > 0 && m > 0 );
        fwrite(buf, 1, sizeof(buf)-m, fp);
    }
    return N-n;
}

int readpcm24(int32_t *x, int n, FILE *fp)
{
    static char buf[3*256];
    int N = n;
    while(n) {
        int m = 3*(256 < n ? 256 : n);
        char *pbuf = buf;
        m = fread(buf, 1, m, fp);
        if(m == 0) {
            break;
        }
        while(n > 0 && m > 0 ) {
            int val = (((int)(unsigned char)pbuf[2])<<24) | 
                      (((int)(unsigned char)pbuf[1])<<16) |
                      (((int)(unsigned char)pbuf[0])<< 8);
            pbuf += 3;
            *x++ = val;
            m -= 3;
            n -= 1;
        }
    }
    return N-n;
}

